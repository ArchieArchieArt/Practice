/**
 * Дан массив с целыми числами:
 * int[] arr = {1, 2, 3, 4, 5};
 * Найдите сумму элементов этого массива.
 */
public class LessonSumOfArray {

    public static void main(String[] args) {
        int[] arr = {1, 2, 3, 4, 5};

        int a1 = arr[0];
        int a2 = arr[1];
        int a3 = arr[2];
        int a4 = arr[3];
        int a5 = arr[4];

        System.out.println(a1 + a2 + a3 + a4 + a5);
    }
}